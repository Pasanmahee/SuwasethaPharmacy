    private void loadEmployeeTable() {
        tableName = "employee";

        Employee_Models objEmployee_Models = new Employee_Models();
        objEmployee_Models.setEmployeeDetails(tableName);

        Employee_DB objEmployee_DB = new Employee_DB();
        ResultSet rs = objEmployee_DB.loadEmployeeTable(objEmployee_Models);
        try {
            if (rs.next()) {

                tbl_employee.setModel(DbUtils.resultSetToTableModel(rs));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No Employee Table Found");
        }

    }

