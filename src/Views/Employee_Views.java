    private void tbl_employeeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_employeeMouseClicked

        int row = tbl_employee.getSelectedRow();
        String tableClick = tbl_employee.getModel().getValueAt(row, 0).toString();

        Employee_Models objEmployee_Models = new Employee_Models();
        objEmployee_Models.setEmployeeID(tableClick);
        Employee_DB objEmployee_DB = new Employee_DB();
        ResultSet rs = objEmployee_DB.loadEmployeeTableID(objEmployee_Models);
        try {
            if (rs.next()) {

                txt_EmployeeID.setText(rs.getString("Employee_ID"));
                txt_Address.setText(rs.getString("Address"));
                int intAge = rs.getInt("Age");
                String strAge = String.valueOf(intAge);
                txt_Age.setText(strAge);
                txt_Name.setText(rs.getString("Employee_Name"));
                txt_Qualifications.setText(rs.getString("Qualifications"));
                txt_TelephoneNo.setText(rs.getString("Telephone_No"));
                txt_Email.setText(rs.getString("Email"));
                
                java.util.Date d = rs.getDate("Join_Date");
                jDateChooser1.setDate(d);
                

            } else {
                JOptionPane.showMessageDialog(null, "Error");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        loadImage();
    }//GEN-LAST:event_tbl_employeeMouseClicked

    private void txt_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_searchKeyReleased
        Employee_Models objEmployee_Models = new Employee_Models();
        String value = txt_search.getText();
        Employee_DB objEmployee_DB = new Employee_DB();

        if (rb_EmployeeID.isSelected()) {

            objEmployee_Models.setEmployeeID(value);
        } else if (rb_EmployeeName.isSelected()) {

            objEmployee_Models.setEmployeeName(value);
        } else if (rb_Address.isSelected()) {

            objEmployee_Models.setAddress(value);
        } else if (rb_Age.isSelected()) {
            int intValue = Integer.parseInt(value);
            objEmployee_Models.setAge(intValue);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a Button before you search");
        }
        ResultSet rs = objEmployee_DB.getSearchDetails(objEmployee_Models);
        tbl_employee.setModel(DbUtils.resultSetToTableModel(rs));

    }//GEN-LAST:event_txt_searchKeyReleased

    private void btn_BrowseImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_BrowseImageActionPerformed

        JFileChooser filechooser = new JFileChooser();
        filechooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.IMAGE", "jpg", "png");
        filechooser.addChoosableFileFilter(filter);
        int result = filechooser.showSaveDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = filechooser.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            lbl_employeeImage.setIcon(ResizeImage(path));
            s = path;

            try {
                File image = new File(s);
                FileInputStream fis = new FileInputStream(image);
                ByteArrayOutputStream bos = new ByteArrayOutputStream();
                byte[] buf = new byte[2000000];//Image size in bytes 
                for (int readNum; (readNum = fis.read(buf)) != -1;) {
                    bos.write(buf, 0, readNum);
                }
                employeeImage = bos.toByteArray();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Your Image is too large");
            }

        } else if (result == JFileChooser.CANCEL_OPTION) {
            JOptionPane.showMessageDialog(null, "No Data");
        }
    }//GEN-LAST:event_btn_BrowseImageActionPerformed

