    private void loadCustomerTable() {
        tableName = "customer";

        Customer_Models objCustomer_Models = new Customer_Models();
        objCustomer_Models.setCustomerDetails(tableName);

        Customer_DB objCustomer_DB = new Customer_DB();
        ResultSet rs = objCustomer_DB.loadCustomerTable(objCustomer_Models);
        try {
            if (rs.next()) {

                tbl_customer.setModel(DbUtils.resultSetToTableModel(rs));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No Customer Table Found");
        }

    }

    private void loadImage() {

        String getCustomerID = txt_customerID.getText();
        try {

            Customer_Models objCustomer_Models = new Customer_Models();
            objCustomer_Models.setCustomerID(getCustomerID);
            Customer_DB objCustomer_DB = new Customer_DB();
            ResultSet rs = objCustomer_DB.loadImage(objCustomer_Models);

            if (rs.next()) {
                byte[] imgData = rs.getBytes("Image");
                ImageIcon image = new ImageIcon(imgData);
                Image im = image.getImage();
                Image myimg = im.getScaledInstance(lbl_customerImage.getWidth(), lbl_customerImage.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon newImage = new ImageIcon(myimg);
                lbl_customerImage.setIcon(newImage);

            } else {
                //JOptionPane.showMessageDialog(null, "No Image Data");

            }

        } catch (Exception e) {
            ImageIcon image = new ImageIcon("E:\\HNDJava\\Suwasetha_Pharmacy\\src\\suwasetha_pharmacy\\profilePic.png");
            lbl_customerImage.setIcon(image);
        }

    }

