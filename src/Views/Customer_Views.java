    private void btn_browseImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_browseImageActionPerformed

        JFileChooser filechooser = new JFileChooser();
        filechooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.IMAGE", "jpg", "png");
        filechooser.addChoosableFileFilter(filter);
        int result = filechooser.showSaveDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = filechooser.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            lbl_customerImage.setIcon(ResizeImage(path));
            s = path;

            try {
                File image = new File(s);
                FileInputStream fis = new FileInputStream(image);
                ByteArrayOutputStream bos = new ByteArrayOutputStream();
                byte[] buf = new byte[2000000];//Image size in bytes 
                for (int readNum; (readNum = fis.read(buf)) != -1;) {
                    bos.write(buf, 0, readNum);
                }
                customerImage = bos.toByteArray();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Your Image is too large");
            }

        } else if (result == JFileChooser.CANCEL_OPTION) {
            JOptionPane.showMessageDialog(null, "No Data");
        }

    }//GEN-LAST:event_btn_browseImageActionPerformed

    private void txt_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_searchKeyReleased
        Customer_Models objCustomer_Models = new Customer_Models();
        String value = txt_search.getText();
        Customer_DB objCustomer_DB = new Customer_DB();

        if (rb_customerID.isSelected()) {

            objCustomer_Models.setCustomerID(value);
        } else if (rb_customerName.isSelected()) {

            objCustomer_Models.setCustomerName(value);
        } else if (rb_address.isSelected()) {

            objCustomer_Models.setAddress(value);
        } else if (rb_age.isSelected()) {
            int intValue = Integer.parseInt(value);
            objCustomer_Models.setAge(intValue);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a Button before you search");
        }
        ResultSet rs = objCustomer_DB.getSearchDetails(objCustomer_Models);
        tbl_customer.setModel(DbUtils.resultSetToTableModel(rs));

    }//GEN-LAST:event_txt_searchKeyReleased

    private void tbl_customerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_customerMouseClicked

        int row = tbl_customer.getSelectedRow();
        String tableClick = tbl_customer.getModel().getValueAt(row, 0).toString();

        Customer_Models objCustomer_Models = new Customer_Models();
        objCustomer_Models.setCustomerID(tableClick);
        Customer_DB objCustomer_DB = new Customer_DB();
        ResultSet rs = objCustomer_DB.loadCustomerTableID(objCustomer_Models);
        try {
            if (rs.next()) {

                txt_customerID.setText(rs.getString("Customer_ID"));
                txt_address.setText(rs.getString("Address"));
                int intAge = rs.getInt("Age");
                String strAge = String.valueOf(intAge);
                txt_Age.setText(strAge);
                int intLoyaltyCardFees = rs.getInt("Loyalty_card_fees");
                String strLoyaltyCardFees = String.valueOf(intLoyaltyCardFees);
                txt_loyaltyCard.setText(strLoyaltyCardFees);
                txt_customerName.setText(rs.getString("Customer_Name"));
                txt_medicalHistory.setText(rs.getString("Medical_History"));
                txt_TelephoneNo.setText(rs.getString("Telephone_No"));
                txt_Email.setText(rs.getString("Email"));

            } else {
                JOptionPane.showMessageDialog(null, "Error");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        loadImage();
        loadLoyaltyCard();
    }//GEN-LAST:event_tbl_customerMouseClicked

